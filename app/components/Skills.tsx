function SkillCard({
  category,
  index,
}: {
  category: SkillCategory;
  index: number;
}) {
  const { ref, isVisible } = useScrollAnimation(0.1);

  return (
    <div
      ref={ref}
      className={`group rounded-xl border border-border bg-card/30 p-6 hover:border-border-light transition-all duration-500 ${
        isVisible ? "opacity-100 translate-y-0" : "opacity-0 translate-y-8"
      }`}
      style={{ transitionDelay: `${index * 80}ms` }}
    >
      <div className="flex items-center justify-between mb-5">
        <div className="flex items-center gap-3">
          <span className="text-accent">
            {iconMap[category.iconId] || defaultIcon}
          </span>
          <h3 className="font-semibold text-text-primary font-mono">
            {category.title}
          </h3>
        </div>
        <span className="text-text-muted font-mono text-xs">
          {category.skills.length}
        </span>
      </div>

      <div className="flex flex-wrap gap-2">
        {category.skills.map((skill) => (
          <span
            key={skill}
            className="px-3 py-1.5 text-xs font-medium rounded-lg bg-surface text-text-secondary border border-border/30 hover:border-accent/30 hover:text-accent transition-colors duration-200"
          >
            {skill}
          </span>
        ))}
      </div>
    </div>
  );
}

